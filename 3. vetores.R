# Vetores
# Quando se deseja criar um vetor com mais de um elemento, usa-se a função c() que significa combinar os elementos em um vetor.

# Criar um vetor
maca <- c('vermelha', 'verde', "amarela")
print(maca)

# Obtém a classe do vetor
print(class(maca))

